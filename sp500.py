def listar_sp500():
    return TICKERS_SP500.copy()


def buscar_sp500_por_nome(nome):
    nome = nome.strip().lower()
    if not nome or len(nome) < 2:
        return []
    resultados = []
    for t in TICKERS_SP500:
        if nome in t.lower():
            resultados.append(t)
    return resultados


def buscar_sp500_wikipedia():
    import urllib.request
    import json
    url = ("https://en.wikipedia.org/w/api.php?"
           "action=parse&page=List_of_S%26P_500_companies&"
           "prop=text&section=1&format=json")
    try:
        with urllib.request.urlopen(url, timeout=10) as resp:
            data = json.loads(resp.read().decode())
        html = data["parse"]["text"]["*"]
        import re
        tickers = re.findall(r'<td>([A-Z]+)</td>', html)
        if tickers:
            return tickers
        return None
    except Exception:
        return None


def atualizar_sp500():
    novos = buscar_sp500_wikipedia()
    if novos and len(novos) >= 400:
        print(f"S&P 500 atualizado: {len(novos)} tickers encontrados")
        return novos
    print("Falha ao atualizar S&P 500. Usando lista estatica.")
    return TICKERS_SP500
